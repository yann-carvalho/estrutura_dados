# 1. Faça um programa que calcule a média de três números inseridos pelo usuário.

n1 = float(input('Insira o 1º número: '))
n2 = float(input('Insira o 2º número: '))
n3 = float(input('Insira o 3º número: '))

media = (n1 + n2 + n3) / 3

print(f'A média dos números inseridos no programa é {media:.2f}.')